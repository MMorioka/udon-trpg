export class UdonCondition  {
    noodles: string;
    soup: string;
    howToBoil: string;
    toppings: string;
    seasonings: string;

    constructor(noodles: string, soup: string, howToBoil: string, toppings: string, seasonings: string) {
        this.noodles = noodles;
        this.soup = soup;
        this.howToBoil = howToBoil;
        this.toppings = toppings;
        this.seasonings = seasonings;
    }
}